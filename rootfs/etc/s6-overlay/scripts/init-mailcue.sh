# -------------------------------------------------------------------------
echo "[init-mailcue] Step 9: Setting up GPG keyring directory..."
mkdir -p /var/lib/mailcue/gpg
chmod 700 /var/lib/mailcue/gpg
echo "[init-mailcue] GPG keyring directory ready"

echo "[init-mailcue] Initialisation complete."
